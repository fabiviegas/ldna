const summary1 = document.querySelector('#faqQuestion1');

summary1.addEventListener('click', function(){
    if(summary1.firstElementChild.classList.contains('fa-arrow-right')) {

        summary1.firstElementChild.classList.remove('fa-arrow-right');
        summary1.firstElementChild.classList.add('fa-arrow-down');
    } 
    
    else {
        summary1.firstElementChild.classList.remove('fa-arrow-down');
        summary1.firstElementChild.classList.add('fa-arrow-right');
    }
});

